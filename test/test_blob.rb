require "test_helper"

class TestBlob < Minitest::Test
  def test_deserialize
    input = [0x62, 0x6c, 0x6f, 0x62, 0x20, 0x31, 0x32, 0x00, 0x48, 0x65, 0x6c, 0x6c, 0x6f, 0x20, 0x57, 0x6f, 0x72, 0x6c, 0x64, 0x0a]
    blob = Rgit::Blob.deserialize(input)

    assert_kind_of Rgit::Blob, blob
    assert_equal "Hello World\n", blob.content
  end
end
